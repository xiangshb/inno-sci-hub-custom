import { useState, useEffect } from 'react';
import { Link } from 'react-router-dom';
import { motion } from 'framer-motion';
import { Brain, Calendar, TrendingUp, Search, Filter } from 'lucide-react';
import { Button } from '@/components/ui/button';
import { Card } from '@/components/ui/card';
import { Input } from '@/components/ui/input';
import { Badge } from '@/components/ui/badge';
import { BaseCrudService } from '@/integrations';
import { ScientificInsights } from '@/entities';

export default function InsightsPage() {
  const [insights, setInsights] = useState<ScientificInsights[]>([]);
  const [filteredInsights, setFilteredInsights] = useState<ScientificInsights[]>([]);
  const [searchTerm, setSearchTerm] = useState('');
  const [selectedModel, setSelectedModel] = useState<string>('all');

  useEffect(() => {
    const fetchInsights = async () => {
      try {
        const { items } = await BaseCrudService.getAll<ScientificInsights>('scientificinsights');
        setInsights(items);
        setFilteredInsights(items);
      } catch (error) {
        console.error('Error fetching insights:', error);
      }
    };

    fetchInsights();
  }, []);

  useEffect(() => {
    let filtered = insights;

    if (searchTerm) {
      filtered = filtered.filter(insight =>
        insight.title?.toLowerCase().includes(searchTerm.toLowerCase()) ||
        insight.summary?.toLowerCase().includes(searchTerm.toLowerCase()) ||
        insight.keywords?.toLowerCase().includes(searchTerm.toLowerCase())
      );
    }

    if (selectedModel !== 'all') {
      filtered = filtered.filter(insight => insight.aiModelUsed === selectedModel);
    }

    setFilteredInsights(filtered);
  }, [searchTerm, selectedModel, insights]);

  const uniqueModels = Array.from(new Set(insights.map(insight => insight.aiModelUsed).filter(Boolean)));

  const getRelevanceColor = (score?: number) => {
    if (!score) return 'bg-gray-100 text-gray-800';
    if (score >= 0.8) return 'bg-green-100 text-green-800';
    if (score >= 0.6) return 'bg-yellow-100 text-yellow-800';
    return 'bg-red-100 text-red-800';
  };

  return (
    <div className="min-h-screen bg-background">
      {/* Navigation */}
      <nav className="w-full bg-primary border-b border-subtleborder">
        <div className="max-w-[100rem] mx-auto px-8 py-6">
          <div className="flex justify-between items-center">
            <Link to="/" className="text-primary-foreground font-paragraph text-sm tracking-wider hover:text-secondary transition-colors">
              ‚Üê SCIENTIFIC DISCOVERY PLATFORM
            </Link>
            <div className="hidden md:flex space-x-8 text-primary-foreground font-paragraph text-sm">
              <Link to="/insights" className="text-secondary">Insights</Link>
              <Link to="/research-plans" className="hover:text-secondary transition-colors">Research Plans</Link>
              <Link to="/agents" className="hover:text-secondary transition-colors">AI Agents</Link>
              <Link to="/tools" className="hover:text-secondary transition-colors">Tools</Link>
              <Link to="/knowledge" className="hover:text-secondary transition-colors">Knowledge Network</Link>
              <Link to="/visualizations" className="hover:text-secondary transition-colors">Visualizations</Link>
            </div>
          </div>
        </div>
      </nav>

      {/* Header */}
      <section className="w-full bg-primary">
        <div className="max-w-[100rem] mx-auto px-8 py-16">
          <motion.div
            initial={{ opacity: 0, y: 20 }}
            animate={{ opacity: 1, y: 0 }}
            className="text-center"
          >
            <h1 className="font-heading text-6xl text-primary-foreground mb-6">Scientific Insights</h1>
            <p className="font-paragraph text-primary-foreground/80 text-lg max-w-3xl mx-auto">
              Discover breakthrough scientific insights generated by our advanced AI engines. Each insight represents a potential paradigm shift in human understanding.
            </p>
          </motion.div>
        </div>
      </section>

      {/* Filters and Search */}
      <section className="w-full bg-background border-b border-subtleborder">
        <div className="max-w-[100rem] mx-auto px-8 py-8">
          <div className="flex flex-col md:flex-row gap-6 items-center justify-between">
            <div className="flex-1 max-w-md">
              <div className="relative">
                <Search className="absolute left-3 top-1/2 transform -translate-y-1/2 text-darktext/40 w-4 h-4" />
                <Input
                  placeholder="Search insights, keywords, or topics..."
                  value={searchTerm}
                  onChange={(e) => setSearchTerm(e.target.value)}
                  className="pl-10 border-subtleborder focus:border-secondary"
                />
              </div>
            </div>
            
            <div className="flex items-center gap-4">
              <Filter className="w-4 h-4 text-darktext/60" />
              <select
                value={selectedModel}
                onChange={(e) => setSelectedModel(e.target.value)}
                className="border border-subtleborder rounded-md px-3 py-2 font-paragraph text-sm focus:border-secondary focus:outline-none"
              >
                <option value="all">All AI Models</option>
                {uniqueModels.map(model => (
                  <option key={model} value={model}>{model}</option>
                ))}
              </select>
            </div>
          </div>
        </div>
      </section>

      {/* Insights Grid */}
      <section className="w-full">
        <div className="max-w-[100rem] mx-auto px-8 py-16">
          <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 gap-8">
            {filteredInsights.map((insight, index) => (
              <motion.div
                key={insight._id}
                initial={{ opacity: 0, y: 20 }}
                animate={{ opacity: 1, y: 0 }}
                transition={{ delay: index * 0.1 }}
              >
                <Card className="bg-background border-subtleborder p-6 h-full hover:shadow-lg transition-shadow">
                  <div className="flex items-start justify-between mb-4">
                    <Badge className={`${getRelevanceColor(insight.relevanceScore)} border-0`}>
                      Relevance: {insight.relevanceScore ? (insight.relevanceScore * 100).toFixed(0) : 'N/A'}%
                    </Badge>
                    <Brain className="w-5 h-5 text-secondary" />
                  </div>

                  <h3 className="font-heading text-xl text-darktext mb-3 leading-tight">
                    {insight.title}
                  </h3>

                  <p className="font-paragraph text-darktext/80 text-sm leading-relaxed mb-4">
                    {insight.summary}
                  </p>

                  <div className="space-y-3 mb-6">
                    <div className="flex items-center text-xs text-darktext/60">
                      <Calendar className="w-3 h-3 mr-2" />
                      {insight.discoveryDate ? new Date(insight.discoveryDate).toLocaleDateString() : 'Date unknown'}
                    </div>
                    
                    {insight.aiModelUsed && (
                      <div className="flex items-center text-xs text-darktext/60">
                        <Brain className="w-3 h-3 mr-2" />
                        {insight.aiModelUsed}
                      </div>
                    )}

                    {insight.keywords && (
                      <div className="flex flex-wrap gap-1 mt-3">
                        {insight.keywords.split(',').slice(0, 3).map((keyword, idx) => (
                          <Badge key={idx} variant="outline" className="text-xs border-subtleborder text-darktext/60">
                            {keyword.trim()}
                          </Badge>
                        ))}
                      </div>
                    )}
                  </div>

                  <Button 
                    variant="outline" 
                    className="w-full border-secondary text-secondary hover:bg-secondary hover:text-secondary-foreground"
                  >
                    View Details
                  </Button>
                </Card>
              </motion.div>
            ))}
          </div>

          {filteredInsights.length === 0 && (
            <div className="text-center py-16">
              <Brain className="w-16 h-16 text-darktext/20 mx-auto mb-4" />
              <h3 className="font-heading text-2xl text-darktext/60 mb-2">No insights found</h3>
              <p className="font-paragraph text-darktext/40">
                Try adjusting your search terms or filters to discover more insights.
              </p>
            </div>
          )}
        </div>
      </section>

      {/* Stats Section */}
      <section className="w-full bg-mutedolive/10">
        <div className="max-w-[100rem] mx-auto px-8 py-16">
          <div className="grid grid-cols-1 md:grid-cols-3 gap-8 text-center">
            <motion.div
              initial={{ opacity: 0, y: 20 }}
              whileInView={{ opacity: 1, y: 0 }}
              viewport={{ once: true }}
            >
              <div className="text-4xl font-heading text-secondary mb-2">{insights.length}</div>
              <div className="font-paragraph text-darktext/80">Total Insights Generated</div>
            </motion.div>
            
            <motion.div
              initial={{ opacity: 0, y: 20 }}
              whileInView={{ opacity: 1, y: 0 }}
              viewport={{ once: true }}
              transition={{ delay: 0.1 }}
            >
              <div className="text-4xl font-heading text-secondary mb-2">{uniqueModels.length}</div>
              <div className="font-paragraph text-darktext/80">AI Models Deployed</div>
            </motion.div>
            
            <motion.div
              initial={{ opacity: 0, y: 20 }}
              whileInView={{ opacity: 1, y: 0 }}
              viewport={{ once: true }}
              transition={{ delay: 0.2 }}
            >
              <div className="text-4xl font-heading text-secondary mb-2">
                {insights.filter(i => i.relevanceScore && i.relevanceScore >= 0.8).length}
              </div>
              <div className="font-paragraph text-darktext/80">High-Impact Discoveries</div>
            </motion.div>
          </div>
        </div>
      </section>

      {/* Call to Action */}
      <section className="w-full bg-primary">
        <div className="max-w-[100rem] mx-auto px-8 py-16 text-center">
          <motion.div
            initial={{ opacity: 0, y: 20 }}
            whileInView={{ opacity: 1, y: 0 }}
            viewport={{ once: true }}
          >
            <h2 className="font-heading text-4xl text-primary-foreground mb-6">
              Ready to Generate New Insights?
            </h2>
            <p className="font-paragraph text-primary-foreground/80 text-lg mb-8 max-w-2xl mx-auto">
              Connect with our AI research agents to explore new frontiers in scientific discovery.
            </p>
            <div className="flex flex-col sm:flex-row gap-4 justify-center">
              <Link to="/agents">
                <Button size="lg" className="bg-secondary text-secondary-foreground hover:bg-secondary/90">
                  Explore AI Agents
                </Button>
              </Link>
              <Link to="/research-plans">
                <Button size="lg" variant="outline" className="border-primary-foreground text-primary-foreground hover:bg-primary-foreground hover:text-primary">
                  View Research Plans
                </Button>
              </Link>
            </div>
          </motion.div>
        </div>
      </section>
    </div>
  );
}